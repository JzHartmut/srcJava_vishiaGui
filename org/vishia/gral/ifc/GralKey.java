package org.vishia.gral.ifc;


/**This class declares key codes in a system independent form. The keys are the usual PC-Keyboard combinations.
 * @author hartmut
 *
 */
public class GralKey
{
  public final static int F1 =       0x00f1;
  public final static int aF1 =     0xa00f1;
  public final static int cF1 =     0xc00f1;
  public final static int caF1 =   0xca00f1;
  public final static int scaF1 = 0x5ca00f1;
  public final static int saF1 =   0x5a00f1;
  public final static int scF1 =   0x5c00f1;
  public final static int sF1 =     0x500f1;
  
}
